        $io->definitionList(
            ['URL' => $url],
            ['Сообщений всего' => $count],
            ['Chat 1 (Авторынок)' => $chatId1],
            ['Chat 2 (Туризм)' => $chatId2],
            ['Chat 3 (Жильцы дома)' => $chatId3],
            ['Задержка' => $delayMs.' мс'],
            ['Участников' => \count(self::USERS)],
        );

        $poolAuto = \array_slice(self::MESSAGE_POOL, 0, 300);
        $updates1 = $this->generateUpdates(self::MESSAGES_PER_CHAT, $chatId1, 'Авторынок [тест]', $poolAuto, 0);
        $updates2 = $this->generateUpdates(self::MESSAGES_PER_CHAT, $chatId2, 'Туризм [тест]', self::TOURISM_MESSAGE_POOL, self::MESSAGES_PER_CHAT);
        $updates3 = $this->generateUpdates(self::MESSAGES_PER_CHAT, $chatId3, 'Жильцы дома [тест]', self::RESIDENTS_MESSAGE_POOL, self::MESSAGES_PER_CHAT * 2);

        $updates = array_merge($updates1, $updates2, $updates3);
        shuffle($updates);

        $progressBar = new ProgressBar($output, $count);
        $progressBar->setFormat(' %current%/%max% [%bar%] %percent:3s%% | OK: %ok% | Err: %err% | %elapsed:6s%');
        $progressBar->setMessage('0', 'ok');
        $progressBar->setMessage('0', 'err');
        $progressBar->start();

        $ok = 0;
        $err = 0;

        foreach ($updates as $update) {
            try {
                $response = $this->httpClient->request('POST', $url, [
                    'headers' => [
                        'Content-Type' => 'application/json',
                        'X-Telegram-Bot-Api-Secret-Token' => $this->webhookSecret,
                    ],
                    'json' => $update,
                    'timeout' => 10,
                ]);

                if (200 === $response->getStatusCode()) {
                    ++$ok;
                } else {
                    ++$err;
                    if ($output->isVerbose()) {
                        $io->warning(\sprintf(
                            'HTTP %d: %s',
                            $response->getStatusCode(),
                            $response->getContent(false),
                        ));
                    }
                }
            } catch (\Throwable $e) {
                ++$err;
                if ($output->isVerbose()) {
                    $io->error($e->getMessage());
                }
            }

            $progressBar->setMessage((string) $ok, 'ok');
            $progressBar->setMessage((string) $err, 'err');
            $progressBar->advance();

            if ($delayMs > 0) {
                usleep($delayMs * 1000);
            }
        }

        $progressBar->finish();
        $output->writeln('');

        $io->success(\sprintf('Готово: отправлено %d, успешно %d, ошибок %d.', $count, $ok, $err));

        if ($err > 0) {
            $io->warning('Есть ошибки — запустите с флагом -v для деталей.');
        }

        $io->section('Следующие шаги');
        $io->listing([
            'Воркеры в Docker уже обрабатывают очередь incoming_messages (Supervisor)',
            \sprintf('Проверьте сообщения в БД: SELECT COUNT(*) FROM messages WHERE group_id IN (SELECT id FROM telegram_groups WHERE telegram_chat_id IN (%d, %d, %d));', $chatId1, $chatId2, $chatId3),
            'Кирпичики создаются кроном каждые 3 мин: php bin/console app:dispatch-summary-jobs',
            'Или запустите вручную прямо сейчас: docker compose exec php php bin/console app:dispatch-summary-jobs',
            \sprintf('Проверьте кирпичики: SELECT COUNT(*) FROM contexts WHERE group_id IN (SELECT id FROM telegram_groups WHERE telegram_chat_id IN (%d, %d, %d));', $chatId1, $chatId2, $chatId3),
            \sprintf('Проверьте агрегированные контексты: SELECT * FROM aggregated_contexts_for_group WHERE group_id IN (SELECT id FROM telegram_groups WHERE telegram_chat_id IN (%d, %d, %d));', $chatId1, $chatId2, $chatId3),
        ]);

        return Command::SUCCESS;
    }

    /**
     * @param array<string> $messagePool
     *
     * @return list<array<string, mixed>>
     */
    private function generateUpdates(int $count, int $chatId, string $chatTitle, array $messagePool, int $updateIdOffset = 0): array
    {
        $pool = $messagePool;
        $poolSize = \count($pool);
        $userCount = \count(self::USERS);

        shuffle($pool);

        $updates = [];
        $updateIdBase = 500000001 + $updateIdOffset;
        $date = self::BASE_DATE + $updateIdOffset * 10;

        for ($i = 0; $i < $count; ++$i) {
            $user = self::USERS[$i % $userCount];
            $text = $pool[$i % $poolSize];

            $date += random_int(30, 180);

            $updates[] = [
                'update_id' => $updateIdBase + $i,
                'message' => [
                    'message_id' => 1000 + $updateIdOffset + $i,
                    'from' => [
                        'id' => $user['id'],
                        'is_bot' => false,
                        'first_name' => $user['first_name'],
                        'username' => $user['username'],
                        'language_code' => 'ru',
                    ],
                    'chat' => [
                        'id' => $chatId,
                        'title' => $chatTitle,
                        'type' => 'supergroup',
                    ],
                    'date' => $date,
                    'text' => $text,
                ],
            ];
        }

        return $updates;
    }
